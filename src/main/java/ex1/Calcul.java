public class Calcul {

	/**
	 * methode d' additionne de a et b
	 * @param a un nombre
	 * @param b un autre nombre
	 * @return int le resultat de a+b
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * methode de soustrait de a et b
	 * @param a un nombre
	 * @param b un autre nombre
	 * @return int le resultat de a-b
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}